
public class Nelilaskin {

    public static void main(String[] args) {
        double eka = 5;
        double toka = 3;

        System.out.println(eka + " + " + toka + " = " + (eka + toka));
        System.out.println(eka + " - " + toka + " = " + (eka - toka));
        System.out.println(eka + " * " + toka + " = " + (eka * toka));
        System.out.println(eka + " / " + toka + " = " + (eka / toka));

        // Toteuta loput nelilaskimesta tähän.
    }
}
